import nav
import models

FIRST_NAV = nav.FIRST_NAV
SECOND_NAV = 'outline'

class Apps(resource.Resource):
	app = 'app'
	resource = 'apps'
	
	@login_required
	def get(request):
		"""
		响应GET
		"""
		c = RequestContext(request, {
			'first_nav_name': FIRST_NAV,
			'second_navs': nav.get_second_navs(),
			'second_nav_name': SECOND_NAV
		})
		
		return render_to_response('app/apps.html', c)
