FIRST_NAV = 'outline'
SECOND_NAV = 'outline-outline'


class Outline(resource.Resource):
	app = 'outline'
	resource = 'outline'
	
	@login_required
	def get(request):
		c = RequestContext(request, {
			'first_nav_name': FIRST_NAV,
			'second_navs': nav.get_second_navs(),
			'second_nav_name': SECOND_NAV
		})
		
		return render_to_response('outline/outline.html', c)